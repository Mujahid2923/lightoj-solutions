double angle(double a,double b,double c)
{
    return acos(((a*a+b*b)-(c*c))/(2.0*a*b));
}

int main()
{
    int t;
    cin>>t;
    double r1,r2,r3;
    for(int i=1; i<=t; i++)
    {
        scanf("%lf %lf %lf",&r1,&r2,&r3);
        double a=r2+r3;
        double b=r1+r3;
        double c=r1+r2;

        double P=angle(b,c,a);
        double Q=angle(a,c,b);
        double R=angle(b,a,c);

        double s=(a+b+c)/2;
        double area1=sqrt(s*(s-a)*(s-b)*(s-c));
        double area2=0.5*(r1*r1*P+r2*r2*Q+r3*r3*R);
        double ans=area1-area2;

        printf("Case %d: %.10f\n",i,ans);
    }
    return 0;
}





